// Static list of the days of the week
var days = [
    'Sunday',
    'Monday',
    'Tuesday',
    'Wednesday',
    'Thursday',
    'Friday',
    'Saturday'
]

$(document).ready(function() {

    // Utility Functions

    // Initializes Localstorage
    var localStorage = window.localStorage;

    // Sorts out the Unique values for the citys filters
    function uniqueArray(value, index, self) {
        return self.indexOf(value.trim()) === index
    }

    // Formats the ids placed on the filter buttons
    function formatCityString(str) {
        return str.replace(/\s+/g, '-').toLowerCase();
    }

    // Day Filter Functions

    // Creates the elements with the correct id to filter
    days.forEach(function(day) {
        $('#day-filter').append(`<a class="dropdown-item" id="day-${day}-filter">${day}</a>`);
    });

    // Adds the listeners to all the day filter links
    function addDayFilterListeners() {
        days.forEach(function(day) {
            $(`#day-${day}-filter`).click("click", function() {
                dayFilter(day);
            });
        });
    }

    // Adds a listener to the static any day link
    $('#anyday-filter').click('click', function() {
        dayFilter('any');
    });

    // Gender Filter Functions

    function filteredGenders() {
        var found = []
        groups.map(function(group) {
            if (group.gender === localStorage.gender) {
                found.push(group);
            }
        })
        return found;
    }

    // City Filter Functions

    function createFilteredCitys() {
        // Grabs the list of groups based on gender storaged in localStorage
        var filterGendersArray = filteredGenders(groups)
        // Retuns a "Unique Array" of the filterGenersArray. In lamans terms it
        // removes all the duplicate cities for the filter buttons
        var citys = filterGendersArray.map(function(group) {
            return group.location
        }).filter(uniqueArray);

        // Takes the unique array "citys" and creates the filter buttons links
        // with their according id
        citys.forEach(function(city) {
            // Replaces spaces with "-"
            var formattedCity = formatCityString(city);

            $('#city-filter').append(`<a class="dropdown-item" id="${formattedCity}-filter">${city}</a>`)
        });
        // Initializes the onclick listeners for all the links
        addCityFilters(citys)
    }

    function addCityFilters(citys) {
        citys.forEach(function(city) {
            var formattedCity = formatCityString(city);
            $(`#${formattedCity}-filter`).click("click", function() {
                cityFilter(formattedCity);
            });
        });
    }

    // List.js Functions and Initializers

    // Initializing
    var options = {
        valueNames: [
            "name",
            "location",
            "dayofweek",
            "time",
            "kids",
            {
                name: "signuplink",
                attr: "href"
            }
        ]
    };

    var groupsList = new List('groups-list', options, groups);

    // Functions

    function dayFilter(day) {
        groupsList.filter(function(group) {
            var values = group.values();
            // If any day is selected it just returns all within the gender selected
            if (day === 'any' && values.gender === localStorage.gender) {
                return true
            }
            // As you will see the ""&& values.gender === localStorage.gender" which
            // keeps things the list gender filtered along with the new filter.
            if (values.dayofweek === day && values.gender === localStorage.gender) {
                return true;
            } else {
                return false;
            }
        })
    }

    function cityFilter(city) {
        groupsList.filter(function(group) {
            var values = group.values();
            if (formatCityString(values.location) === city && values.gender === localStorage.gender) {
                return true;
            } else {
                return false;
            }
        });
    }

    function genderFilter() {
        groupsList.filter(function(group) {
            if (group.values().gender === localStorage.gender) {
                return true;
            } else {
                return false;
            }
        })
    }

    function childcareFilter(val) {
        groupsList.filter(function(group) {
            var values = group.values();
            if (values.kids === val & values.gender === localStorage.gender) {
                return true;
            } else {
                return false;
            }
        })
    }

    // Clears all filters and defaults to whatever is the gender stored in local storage
    function clearFilters() {
        genderFilter();
    }

    // Adding listeners for filters
    $('#clear-filters').click("click", clearFilters);
    $('#no-childcare').click("click", function() {
        childcareFilter('No');
    });
    $('#yes-childcare').click("click", function() {
        childcareFilter('Yes');
    });

    // Google Map

    // Initializes map and defaults to the men groups url
    $('<iframe src="https://www.google.com/maps/d/u/0/embed?mid=1LAWWyZCiU7v8otNHZt_U6Us6RC7ApkWJ" width="640" height="480"></iframe>')
        .appendTo('#map');

    // Addes click listeners to the buttons when choosing men or womens small group
    $('#women-selector').click("click", womenSelector);
    $('#men-selector').click("click", menSelector);
    $('#mobile-women-selector').click("click", womenSelector);
    $('#mobile-men-selector').click("click", menSelector);


    // Removes the iframe when changing groups
    function emptyMap() {
        $('#map').empty();
    }

    function emptyCityFilters() {
        $('#city-filter').empty();
    }

    function womenSelector() {
        localStorage.setItem('gender', 'female');
        emptyCityFilters()
        createFilteredCitys();
        genderFilter();
        emptyMap();
        $('#mens-city-intro').addClass("hide");
        $('#womens-city-intro').removeClass("hide");
        $('#mobile-womens-city-intro').removeClass("hide");
        $('#mobile-mens-city-intro').addClass("hide");
        $('#mens-content').addClass("hide");
        $('#womens-content').removeClass("hide");
        $('#mobile-mens-content').addClass("hide");
        $('#mobile-womens-content').removeClass("hide");
        $('#authentic-mens-content').addClass("hide");
        $('#authentic-womens-content').removeClass("hide");
        $('#mobile-authentic-mens-content').addClass("hide");
        $('#mobile-authentic-womens-content').removeClass("hide");
        $('#mens-sub-title').addClass("hide");
        $('#womens-sub-title').removeClass("hide");
        $('#mens-sub-title').addClass("hide");
        $('#womens-sub-title').removeClass("hide");
        $('#mobile-mens-sub-title').addClass("hide");
        $('#mobile-womens-sub-title').removeClass("hide");
        $('<iframe src=" https://www.google.com/maps/d/u/0/embed?mid=1sBsy1oCa4Hk-6XnS7bRpVju5qfcybEXI"></iframe>')
            .appendTo('#map');
    }

    function menSelector() {
        localStorage.setItem('gender', 'male');
        emptyCityFilters()
        createFilteredCitys();
        genderFilter();
        emptyMap();
        $('#womens-city-intro').addClass("hide");
        $('#mens-city-intro').removeClass("hide");
        $('#mobile-womens-city-intro').addClass("hide");
        $('#mobile-mens-city-intro').removeClass("hide");
        $('#womens-content').addClass("hide");
        $('#mens-content').removeClass("hide");
        $('#mobile-womens-content').addClass("hide");
        $('#mobile-mens-content').removeClass("hide");
        $('#authentic-womens-content').addClass("hide");
        $('#authentic-mens-content').removeClass("hide");
        $('#mobile-authentic-womens-content').addClass("hide");
        $('#mobile-authentic-mens-content').removeClass("hide");
        $('#womens-sub-title').addClass("hide");
        $('#mens-sub-title').removeClass("hide");
        $('#mobile-womens-sub-title').addClass("hide");
        $('#mobile-mens-sub-title').removeClass("hide");
        $('<iframe src=" https://www.google.com/maps/d/u/0/embed?mid=1LAWWyZCiU7v8otNHZt_U6Us6RC7ApkWJ"></iframe>')
            .appendTo('#map');
    }

    // Initializes Everything
    localStorage.setItem('gender', 'male');
    addDayFilterListeners();
    genderFilter();
    createFilteredCitys();

    $('.signuplink').css({
        "color": "#292b2c",
        "text-decoration": "none",
        "display": "inline-block",
        "padding": "6px 12px",
        "margin-bottom": "0",
        "font-size": "14px",
        "font-weight": "normal",
        "line-height": "1.428571429",
        "text-align": "center",
        "white-space": "nowrap",
        "vertical-align": "middle",
        "cursor": "pointer",
        "border": "1px solid #212529",
        "-webkit-user-select": "none",
        "-moz-user-select": "none",
        "-ms-user-select": "none",
        "-o-user-select": "none",
        "user-select": "none"
    });

});
